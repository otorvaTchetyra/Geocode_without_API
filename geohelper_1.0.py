import geocoder

# Получаем местоположение по IP-адресу
g = geocoder.ip('me')

# Выводим информацию о местоположении
if g.ok:
    print(f"Ваше местоположение: {g.city}, {g.state}, {g.country}")
    print(f"Широта: {g.latlng[0]}, Долгота: {g.latlng[1]}")
else:
    print("Не удалось получить местоположение.")


#Этот код определяет ваше местоположение по IP-адресу и выводит город, штат и страну, а также широту и долготу.